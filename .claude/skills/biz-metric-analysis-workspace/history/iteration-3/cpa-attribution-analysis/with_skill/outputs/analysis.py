print(f"  消耗 +15 万（+30%），但新客 -2,083 人（-18.7%）")
print(f"  CPA 从 45 元上涨至 72 元（+60%）")
print(f"  ")

# 计算信息流的真实效率损失
信息流_上周千次成本 = 65 * 10000 / (9028 / 1000)  # 元/千次
信息流_上上周千次成本 = 50 * 10000 / (11111 / 1000)
print(f"  上上周千次点击成本: {信息流_上上周千次成本:.2f} 元")
print(f"  上周千次点击成本: {信息流_上周千次成本:.2f} 元")
print(f"  ")

# 归因计算
# 信息流渠道 CPA 上涨的贡献
信息流_CPA变化 = 72 - 45
信息流_消耗占比 = 65 / 105
信息流_CPA权重 = 72 * 9028 / (72 * 9028 + 40 * 4500 + 50 * 4400)

