import pandas as pd
import numpy as np

# ============================================================
# 第一步：数据输入
# ============================================================

data = {
    '渠道': ['信息流', '搜索', '短视频', '合计'],
    '上上周消耗_万': [50, 20, 15, 85],
    '上周消耗_万': [65, 18, 22, 105],
    '上上周新客数': [11111, 5000, 4286, 20397],
    '上周新客数': [9028, 4500, 4400, 17928],
    '上上周CPA': [45, 40, 35, 42],
    '上周CPA': [72, 40, 50, 59],
}

df = pd.DataFrame(data)

print("=" * 60)
print("原始数据")
print("=" * 60)
print(df.to_string(index=False))

# ============================================================
# 第二步：验证异动真实性
# ============================================================

print("\n" + "=" * 60)
print("一、异动验证")
print("=" * 60)

total_上周消耗 = 105
total_上上周消耗 = 85
total_上周新客 = 17928
total_上上周新客 = 20397

# 总体 CPA 计算
整体上上周CPA = total_上上周消耗 * 10000 / total_上上周新客
整体上周CPA = total_上周消耗 * 10000 / total_上周新客
CPA变化量 = 整体上周CPA - 整体上上周CPA
CPA变化率 = CPA变化量 / 整体上上周CPA

print(f"上上周整体 CPA: {整体上上周CPA:.2f} 元")
print(f"上周整体 CPA: {整体上周CPA:.2f} 元")
print(f"CPA 变化: +{CPA变化量:.2f} 元 ({CPA变化率:.1%})")
print(f"异动判定: 真实异动（从 42 元上涨至 59 元，涨幅 40.5%）")

# ============================================================
# 第三步：渠道维度贡献度拆解
# ============================================================

print("\n" + "=" * 60)
print("二、渠道维度贡献度拆解")
print("=" * 60)

# 计算各渠道变化量
df['消耗变化_万'] = df['上周消耗_万'] - df['上上周消耗_万']
df['新客变化'] = df['上周新客数'] - df['上上周新客数']
df['CPA变化'] = df['上周CPA'] - df['上周CPA']

# 总体变化
total_消耗变化 = df['消耗变化_万'].iloc[-1]  # 合计行
total_新客变化 = df['新客变化'].iloc[-1]

print(f"\n总体消耗变化: +{total_消耗变化:.0f} 万元")
print(f"总体新客变化: {total_新客变化:,} 人（减少 {abs(total_新客变化):,} 人）")

# 各渠道贡献度计算
print("\n各渠道贡献度明细：")
for idx, row in df.iterrows():
    if row['渠道'] == '合计':
        continue

    消耗贡献 = row['消耗变化_万'] / total_消耗变化 * 100 if total_消耗变化 != 0 else 0
    新客贡献 = row['新客变化'] / total_新客变化 * 100 if total_新客变化 != 0 else 0

    # 计算该渠道对整体 CPA 变化的"责任"
    # CPA 变化 = 消耗变化/新客 - 基准 CPA（简化计算）
    # 实际影响 = 该渠道消耗变化导致的 CPA 增量
    消耗增量 = row['消耗变化_万'] * 10000  # 转为元
    上周单价 = row['上周CPA']  # 该渠道新客成本
    影响量 = 消耗增量 / (total_上周新客 + row['新客变化']) if (total_上周新客 + row['新客变化']) != 0 else 0

    print(f"\n{row['渠道']}：")
    print(f"  消耗变化: {row['消耗变化_万']:+.0f} 万元 (贡献 {消耗贡献:.1f}%)")
    print(f"  新客变化: {row['新客变化']:,} 人 (贡献 {新客贡献:.1f}%)")
    print(f"  CPA 变化: {row['上周CPA'] - row['上上周CPA']:+.0f} 元")

# ============================================================
# 第四步：归因分析
# ============================================================

print("\n" + "=" * 60)
print("三、根因归因")
print("=" * 60)

# 信息流分析
信息流_消耗增量 = 15  # 65-50
