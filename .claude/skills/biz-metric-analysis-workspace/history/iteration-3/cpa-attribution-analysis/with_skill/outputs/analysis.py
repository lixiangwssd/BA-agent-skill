print(f"  - 消耗占比: {信息流_消耗占比:.1%}（65/105万）")
print(f"  - 新客占比: {9028/17928:.1%}（9028/17928）")
print(f"  - CPA 差值: +27 元（72-45）")

# 搜索渠道
搜索_CPA变化 = 0
print(f"\n搜索渠道贡献度: 0%（CPA 不变）")
