#!/usr/bin/env python3
"""
Q1 收入 vs 预算差异分析
财务分析师汇报用 - CFO 专用
"""

import pandas as pd

# ============ 数据定义 ============
data = {
    '区域': ['华东', '华南', '华北', '西部', '合计'],
    '预算收入': [800, 600, 500, 300, 2200],
    '实际收入': [856, 498, 445, 321, 2120],
    '差异': [56, -102, -55, 21, -80]
}

df = pd.DataFrame(data)

# ============ 计算贡献度 ============
# 各区域对总体差异的贡献度（差异占总差异的比例）
total_variance = -80  # 总差异
df['贡献差异额'] = df['差异']
df['贡献度占比'] = df['差异'] / total_variance * 100

# 实际 vs 预算的达成率
df['预算达成率'] = df['实际收入'] / df['预算收入'] * 100

# ============ 输出分析结果 ============
print("=" * 60)
print("Q1 收入 vs 预算差异分析报告")
print("=" * 60)

print("\n【一、核心数据】")
print(df.to_string(index=False))

print("\n【二、差异拆解】")
print(f"总收入差异：{total_variance} 万元")
print(f"预算达成率：{2120/2200*100:.1f}%")

print("\n【三、各区域贡献度分析】")
# 排除合计行
region_df = df[df['区域'] != '合计'].copy()
region_df['贡献度占比'] = region_df['差异'] / abs(total_variance) * 100

for _, row in region_df.iterrows():
    direction = "拖累" if row['差异'] < 0 else "贡献"
    print(f"  {row['区域']}：{row['差异']:+d} 万元，贡献度 {row['贡献度占比']:.1f}%，{direction}")

print("\n【四、关键发现】")
print("1. 拖累项：华南区差异 -102 万元，是最大拖累项")
print("2. 拖累项：华北区差异 -55 万元，拖累作用次之")
print("3. 抵消项：华东区差异 +56 万元，有效抵消了部分下滑")
print("4. 抵消项：西区差异 +21 万元，小幅正向贡献")

print("\n【五、已知背景】")
print("- 华南区：大客户 3 月延期签单（一次性因素，非持续性问题）")

print("\n【六、结论与建议】")
print("结论：")
print("  - Q1 收入低于预算 80 万元，整体达成率 96.4%")
print("  - 华南大客户延期是主要拖累因素（-102 万），若剔除则超额完成预算")
print("  - 华东表现亮眼（+56 万），西部稳定（+21 万）")
print("  - 华北下滑 55 万需关注，但幅度相对可控")

print("\n建议：")
print("  1. 华南：与该大客户保持沟通，争取 4 月补签，弥补缺口")
print("  2. 华北：深入分析下滑原因，判断是否市场环境变化或竞争因素")
print("  3. 华东/西部：总结成功经验，评估是否可复制到其他区域")
print("  4. 全局：Q2 补欠目标 = 80 万元，需关注后续恢复情况")