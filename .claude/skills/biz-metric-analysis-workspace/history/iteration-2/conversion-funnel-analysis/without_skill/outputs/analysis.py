#!/usr/bin/env python3
"""
注册转化率漏斗分析
分析各步骤对整体转化率下降的贡献度
"""

import pandas as pd
import numpy as np

# 漏斗数据
data = {
    '漏斗步骤': ['落地页访问', '点击注册按钮', '填写表单', '提交成功'],
    '上月UV': [500000, 125000, 62500, 16000],
    '本月UV': [520000, 109200, 49140, 10920],
    '上月转化率': [None, 0.250, 0.500, 0.256],
    '本月转化率': [None, 0.210, 0.450, 0.222]
}

df = pd.DataFrame(data)

# 计算各步骤转化率
df['步骤转化率变化'] = df['本月转化率'] - df['上月转化率']
df['步骤转化率变化百分点'] = df['步骤转化率变化'] * 100

# 计算各步骤UV变化对最终转化的影响
# 贡献度 = (本月UV - 上月UV) * 上月转化率 (针对进入步骤的UV变化影响)
# 或者用差值分解方法

# 简化分析：计算各步骤本身转化率下降对整体注册量的影响
# 假设落地页UV是基数，计算各步骤的"转化损失"

# 落地页访问 -> 点击注册按钮
step1_loss_uv = 520000 * (0.210 - 0.250)  # 本月UV * 转化率变化
step1_loss_users = 520000 * 0.210 - 125000 * 0.250  # 实际损失

# 点击注册按钮 -> 填写表单
step2_loss = 109200 * (0.450 - 0.500)

# 填写表单 -> 提交成功
step3_loss = 49140 * (0.222 - 0.256)

# 各步骤贡献度计算（归因）
# 方法：某步骤转化率下降导致的注册损失 / 总注册损失

# 基准：按上月漏斗计算应有的注册量
expected_last = 500000 * 0.250 * 0.500 * 0.256  # 16000
expected_this = 520000 * 0.210 * 0.450 * 0.222  # 10920

total_loss = expected_last - expected_this  # 约5080（实际10920-16000=-5080，取绝对值）

# 各步骤贡献度分解
# 步骤1贡献（点击注册按钮转化率下降）
contrib_step1 = (520000 * 0.210 - 109200)  # 实际减少的点击用户
contrib_step2 = (109200 * 0.450 - 49140)    # 实际减少的表单填写用户
contrib_step3 = (49140 * 0.222 - 10920)     # 实际减少的提交用户

# 归因分析：各步骤转化率下降贡献
attr_step1 = 520000 * (0.210 - 0.250) * 0.500 * 0.256  # 点击转化下降的间接影响
attr_step2 = 109200 * 0.210 * (0.450 - 0.500) * 0.256
attr_step3 = 49140 * 0.210 * 0.450 * (0.222 - 0.256)

print("=" * 60)
print("注册转化率漏斗分析报告")
print("=" * 60)

print("\n【1. 漏斗数据概览】")
print(df.to_string(index=False))

print("\n【2. 整体转化率对比】")
last_month_rate = 16000 / 500000 * 100
this_month_rate = 10920 / 520000 * 100
print(f"上月整体转化率: {last_month_rate:.2f}%")
print(f"本月整体转化率: {this_month_rate:.2f}%")
print(f"变化: {this_month_rate - last_month_rate:.2f} 个百分点")

print("\n【3. 各步骤转化率变化】")
for idx, row in df.iterrows():
    if idx > 0:
        change = row['步骤转化率变化百分点']
        direction = "↓" if change < 0 else "↑"
        print(f"{row['漏斗步骤']}: {row['上月转化率']*100:.1f}% → {row['本月转化率']*100:.1f}% {direction} {abs(change):.1f}个点")

print("\n【4. 各步骤损失用户数分解】")
# 重新计算更准确的贡献度
# 实际注册损失 = 16000 - 10920 = 5080

# 固定本月UV，分析各步骤转化率下降的贡献
uv_fixed = 520000

# 步骤1: 点击注册按钮转化率下降影响
loss_step1_click = uv_fixed * (0.210 - 0.250) * 0.500 * 0.256

# 步骤2: 填写表单转化率下降影响
loss_step2_form = uv_fixed * 0.210 * (0.450 - 0.500) * 0.256

# 步骤3: 提交成功转化率下降影响
loss_step3_submit = uv_fixed * 0.210 * 0.450 * (0.222 - 0.256)

total_decomposition = loss_step1_click + loss_step2_form + loss_step3_submit

print(f"步骤1（点击注册按钮）转化率下降贡献: {loss_step1_click:.0f} 用户")
print(f"步骤2（填写表单）转化率下降贡献: {loss_step2_form:.0f} 用户")
print(f"步骤3（提交成功）转化率下降贡献: {loss_step3_submit:.0f} 用户")
print(f"分解计算总计: {total_decomposition:.0f} 用户")
print(f"实际注册损失: {abs(16000 - 10920):.0f} 用户")

print("\n【5. 贡献度占比】")
contributions = {
    '点击注册按钮': abs(loss_step1_click) / abs(16000 - 10920) * 100,
    '填写表单': abs(loss_step2_form) / abs(16000 - 10920) * 100,
    '提交成功': abs(loss_step3_submit) / abs(16000 - 10920) * 100
}

for step, pct in contributions.items():
    print(f"{step}: {pct:.1f}%")

