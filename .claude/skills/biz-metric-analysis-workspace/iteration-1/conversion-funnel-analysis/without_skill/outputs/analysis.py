import pandas as pd

# 数据
data = {
    '漏斗步骤': ['落地页访问', '点击注册按钮', '填写表单', '提交成功'],
    '上月UV': [500000, 125000, 62500, 16000],
    '本月UV': [520000, 109200, 49140, 10920],
    '上月转化率': [None, 25.0, 50.0, 25.6],
    '本月转化率': [None, 21.0, 45.0, 22.2]
}
df = pd.DataFrame(data)

# 计算各步骤变化
df['UV变化'] = df['本月UV'] - df['上月UV']
df['转化率变化'] = df['本月转化率'] - df['上月转化率']

# 各步骤流失
df['上月流失'] = df['上月UV'] - df['上月UV'].shift(-1)
df['本月流失'] = df['本月UV'] - df['本月UV'].shift(-1)
df['新增流失'] = df['本月流失'] - df['上月流失']

print("注册转化率漏斗分析")
print("=" * 60)
print(f"整体转化率：3.2% → 2.1%，下降1.1个百分点")
print(f"损失注册用户：约5080人")
print()
print("各步骤分析：")
print(df[['漏斗步骤', '上月UV', '本月UV', '转化率变化', '新增流失']].to_string(index=False))

print("\n优先级排序：")
print("1. 点击注册按钮：转化率 -4pp，新增流失最大")
print("2. 填写表单：转化率 -5pp")
print("3. 提交成功：转化率 -3.4pp")
